class InfoRuc {
  String ruC;
  String razonSocial;
  String tipoContribuyente;
  String? tipoDocumento;
  String estadoContribuyente;
  String condicionContribuyente;
  String domicilioFiscal;
  String fechaInscripcion;
  String fechaInicioActividades;

  InfoRuc({
    required this.ruC,
    required this.razonSocial,
    required this.tipoContribuyente,
    required this.tipoDocumento,
    required this.estadoContribuyente,
    required this.condicionContribuyente,
    required this.domicilioFiscal,
    required this.fechaInscripcion,
    required this.fechaInicioActividades,
  });
}
